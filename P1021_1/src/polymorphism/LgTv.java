package polymorphism;

public class LgTv {
    public void turnOn() {
        System.out.println("엘지티비 전원을 켠다.");
    }
    public void turnOff() {
        System.out.println("엘지티비 전원을 끈다.");
    }

    public void soundUp() {
        System.out.println("엘지티비 소리를 올린다.");
    }

    public void soundDown() {
        System.out.println("엘지티비 소리를 내린다.");
    }
}
