public class P2 {
    public static void main(String[] args) {
        // 변수의 선언 문법
        // type 변수명 = 초기값;
        int a = 10;
        int b = 20;
        int c = 30;
        // 중복 선언 불가
        // 같은 타입이 아니면 대입 불가능
        a = 100; // 변수 선언 문법 아님. 대입임

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        
    }
}
